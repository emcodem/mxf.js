export type UL = Uint8Array;

export function ulEquals(a: Uint8Array, b: Uint8Array): boolean {
  if (a.length !== b.length) return false;
  for (let i = 0; i < a.length; i++) {
    if (a[i] !== b[i]) return false;
  }
  return true;
}

/**
 * Match two class ULs as metadata Set keys, ignoring the "item designator/version"
 * bytes at positions 5–7 which vary between encoders and spec versions.
 *
 * A UL key for a metadata Set has this structure:
 *   [0-3]  06 0E 2B 34  — Universal Label prefix
 *   [4]    category      — 02 = Sets & Packs
 *   [5]    item type     — 53 = Local Set (2-byte tag/len) — VARIES by encoder
 *   [6-7]  SMPTE version — VARIES by spec version
 *   [8-15] registry UL   — the object identifier, fixed per class
 *
 * We compare [0..4] and [8..15], skip [5..7].
 */
export function ulMatchClass(key: Uint8Array, classUL: Uint8Array): boolean {
  if (key.length < 16 || classUL.length < 16) return false;
  // bytes 0-4: UL prefix + category
  for (let i = 0; i < 5; i++) {
    if (key[i] !== classUL[i]) return false;
  }
  // bytes 8-15: registry object identifier
  for (let i = 8; i < 16; i++) {
    if (key[i] !== classUL[i]) return false;
  }
  return true;
}

export function ulStartsWith(key: Uint8Array, prefix: Uint8Array): boolean {
  if (key.length < prefix.length) return false;
  for (let i = 0; i < prefix.length; i++) {
    if (key[i] !== prefix[i]) return false;
  }
  return true;
}

function ul(...bytes: number[]): Uint8Array {
  return new Uint8Array(bytes);
}

