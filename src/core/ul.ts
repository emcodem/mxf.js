export function isPrimerPack(key: Uint8Array): boolean {
  return ulEquals(key, UL_PRIMER_PACK) || ulEquals(key, UL_PRIMER_PACK_ALT);
}

export function isPartitionPack(key: Uint8Array): boolean {
  // Exclude Primer Pack — non-conformant encoders write byte 5 = 0x05, making it
  // share the 13-byte prefix with partition packs.
  if (isPrimerPack(key)) return false;
  return ulStartsWith(key, UL_PARTITION_PACK_PREFIX);
}

export function isEssenceElement(key: Uint8Array): boolean {
  return ulStartsWith(key, UL_GC_PICTURE_ITEM_PREFIX) ||
         ulStartsWith(key, UL_GC_SOUND_ITEM_PREFIX) ||
         ulStartsWith(key, UL_GC_DATA_ITEM_PREFIX);
}

export function isPictureEssence(key: Uint8Array): boolean {
  return ulStartsWith(key, UL_GC_PICTURE_ITEM_PREFIX) ||
         ulStartsWith(key, UL_D10_VIDEO_ITEM_PREFIX);
}

export function isSoundEssence(key: Uint8Array): boolean {
  return ulStartsWith(key, UL_GC_SOUND_ITEM_PREFIX) ||
         ulStartsWith(key, UL_D10_SOUND_ITEM_PREFIX);
}

export function isIndexTableSegment(key: Uint8Array): boolean {
  return ulEquals(key, UL_INDEX_TABLE_SEGMENT_V1) || ulEquals(key, UL_INDEX_TABLE_SEGMENT_V2);
}

export function isFill(key: Uint8Array): boolean {
  return ulEquals(key, UL_KLV_FILL);
}
