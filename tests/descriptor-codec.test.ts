import { describe, it, expect } from 'vitest';
import { identifyVideoCodec } from '../src/parser/descriptor.js';

const ul = (...bytes: number[]) => Uint8Array.from(bytes);

describe('identifyVideoCodec (picture essence coding UL → codec)', () => {
  it('recognizes AVC / H.264 with the …02 01 32 label (AVC-Intra), any designator byte', () => {
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x0a,0x04,0x01,0x02,0x02,0x01,0x32,0x01,0x01))).toBe('h264');
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x09,0x04,0x01,0x02,0x02,0x01,0x32,0x00,0x00))).toBe('h264');
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x0d,0x04,0x01,0x02,0x02,0x01,0x32,0x01,0x01))).toBe('h264');
  });

  it('recognizes AVC / H.264 with the …02 01 31 label (Long-GOP XAVC-L), any designator byte', () => {
    // Designators 0x0a and 0x09 — probed from xavc_l_1080p50.mxf (trailing bytes = profile/level).
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x0a,0x04,0x01,0x02,0x02,0x01,0x31,0x40,0x01))).toBe('h264');
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x09,0x04,0x01,0x02,0x02,0x01,0x31,0x40,0x01))).toBe('h264');
    // Designator 0x0d — probed from 1080_50p_709_BeepFlash_XAVCL.mxf (Shogun Max Prater).
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x0d,0x04,0x01,0x02,0x02,0x01,0x31,0x60,0x01))).toBe('h264');
  });

  it('recognizes AVC / H.264 with the …02 01 30 label (SMPTE generic H.264/MPEG-4 AVC)', () => {
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x0a,0x04,0x01,0x02,0x02,0x01,0x30,0x00,0x00))).toBe('h264');
  });

  it('recognizes MPEG-2 (byte-13 = 0x00–0x09)', () => {
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x03,0x04,0x01,0x02,0x02,0x01,0x01,0x01,0x00))).toBe('mpeg2');
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x03,0x04,0x01,0x02,0x02,0x01,0x09,0x00,0x00))).toBe('mpeg2');
  });

  it('returns unknown for MPEG-1 (byte-13 = 0x10)', () => {
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x03,0x04,0x01,0x02,0x02,0x01,0x10,0x00,0x00))).toBe('unknown');
  });

  it('returns unknown for an unrelated label', () => {
    expect(identifyVideoCodec(ul(0x06,0x0e,0x2b,0x34,0x04,0x01,0x01,0x0a,0x04,0x01,0x02,0x02,0x71,0x00,0x00,0x00))).toBe('unknown');
  });
});
